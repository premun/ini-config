namespace Config.Options.NumericValues
{
    /// <summary>
    ///     Numeric format prefixes
    /// </summary>
    public static class NumericPrefix
    {
        /// <summary>
        ///     The hexadecimal format prefix.
        /// </summary>
        public const string Hex = "0x";

        /// <summary>
        ///     The binary format prefix.
        /// </summary>
        public const string Binary = "0b";

        /// <summary>
        ///     The octal format prefix.
        /// </summary>
        public const string Octal = "0";
    }
}